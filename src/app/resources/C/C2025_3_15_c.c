/*
 * [연습문제 8] 삼항 연산자 + 비트 연산 + 논리 연산
 * 
 * 핵심 개념: 삼항 연산자(?:), 비트 시프트(>>), AND(&), 논리 AND(&&)
 * 
 * 아래 코드의 출력 결과를 예측하시오.
 */
#include <stdio.h>

int main(){
    int x = 9, y = 5;
    int r;

    int temp = (y % 4 < 2) ? 3 : 2;
    r = temp | (temp >> 1);

    r = (x > 6 && r <= 5) ? x + r : r - x;

    printf("%d\n", r);
    return 0;
}

/*
 * [풀이 가이드]
 * 
 * 1단계: temp 계산
 *    y % 4 = 5 % 4 = 1
 *    1 < 2 → 참 → temp = 3
 * 
 * 2단계: r 계산 (비트 연산)
 *    temp = 3      → 이진수: 11
 *    temp >> 1 = 1 → 이진수: 01
 *    temp | (temp >> 1) = 3 | 1
 *      11
 *    | 01
 *    ----
 *      11 = 3
 *    r = 3
 * 
 * 3단계: 조건부 연산
 *    x > 6 → 9 > 6 → 참
 *    r <= 5 → 3 <= 5 → 참
 *    둘 다 참 → x + r = 9 + 3 = 12
 *
 * 정답: 12
 */


 // 12