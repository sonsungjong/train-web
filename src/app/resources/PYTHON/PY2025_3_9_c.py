scores = [
    [90, 85],
    [70, 80, 60],
    [100],
    [55, 45, 75, 65]
]

result = {}

for i, row in enumerate(scores):
    avg = sum(row) // len(row)
    result[i] = (sum(row), avg)

print(result)

# 정답: {0: (175, 87), 1: (210, 70), 2: (100, 100), 3: (240, 60)}
# 풀이:
# i=0: row=[90,85]       → sum=175, 175//2=87   → (175, 87)
# i=1: row=[70,80,60]    → sum=210, 210//3=70   → (210, 70)
# i=2: row=[100]         → sum=100, 100//1=100  → (100, 100)
# i=3: row=[55,45,75,65] → sum=240, 240//4=60   → (240, 60)
