data = {'사과': 100, '바나나': 200, '딸기': 300}

data['파인애플'] = 400
data.update({'바나나': 500})
data.pop('딸기')
data['망고'] = 600
data['사과'] = 700

print(f"{len(data) + sum(data.values())}")

# 정답:
# 2204

# 풀이:
# 초기 data = {'사과': 100, '바나나': 200, '딸기': 300}
# data['파인애플'] = 400 → 키-값 쌍 추가
# data.update({'바나나': 500}) → '바나나'의 값을 500으로 수정
# data.pop('딸기') → '딸기' 삭제 (값 300 반환 후 제거됨)
# data['망고'] = 600 → 키-값 쌍 추가
# data['사과'] = 700 → '사과'의 값을 700으로 수정
# 최종 data = {'사과': 700, '바나나': 500, '파인애플': 400, '망고': 600}
# len(data) = 4 (요소 개수)
# sum(data.values()) = 700 + 500 + 400 + 600 = 2200
# 4 + 2200 = 2204
