a = [1, 2, 3, 4, 5]
a = list(map(lambda num : num + 100, a))
print(a)

# 정답:
# [101, 102, 103, 104, 105]

# 풀이:
# a = [1, 2, 3, 4, 5]
# lambda num: num + 100 → 각 원소(num)에 100을 더하는 익명 함수
# map(..., a) → 리스트 a의 각 원소에 람다 함수 적용: [101, 102, 103, 104, 105]
# list(...) → map 객체를 다시 리스트로 변환하여 a에 저장
# print(a) → [101, 102, 103, 104, 105] 출력
