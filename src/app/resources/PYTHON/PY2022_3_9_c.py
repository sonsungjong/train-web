"""
[연습문제] 2022년 3회 기출 대응
핵심 개념: lambda, map()

- map(함수, 리스트) : 리스트의 각 요소에 함수를 적용한 결과를 반환
- lambda : 이름 없는 간단한 함수를 한 줄로 정의
- list()로 감싸야 리스트로 변환됨
"""

# Q. 다음 코드의 실행 결과를 쓰시오.

b = [10, 20, 30, 40, 50]
b = list(map(lambda x : x * 2 - 5, b))
print(b)











# 정답: [15, 35, 55, 75, 95]
# 풀이: 각 요소에 x * 2 - 5 적용
# 10*2-5=15, 20*2-5=35, 30*2-5=55, 40*2-5=75, 50*2-5=95
