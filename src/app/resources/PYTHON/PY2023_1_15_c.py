"""
[연습문제] 2023년 1회 기출 대응
핵심 개념: 집합(Set) - add, remove, update

- add() : 원소 하나 추가 (이미 있으면 무시)
- remove() : 원소 하나 제거
- update() : 여러 원소를 한꺼번에 추가
- 집합은 중복 불가, 출력 순서 무작위
"""

# Q. 다음 코드의 실행 결과를 쓰시오. (순서 무관)

s = {'빨강', '파랑', '초록'}
s.add('노랑')
s.add('빨강')
s.remove('초록')
s.update({'보라', '파랑', '흰색'})
print(s)











# 정답: {'빨강', '파랑', '노랑', '보라', '흰색'} (순서 무관)
# 풀이:
# {'빨강', '파랑', '초록'}
# add('노랑')   → {'빨강', '파랑', '초록', '노랑'}
# add('빨강')   → 이미 있으므로 무시
# remove('초록') → {'빨강', '파랑', '노랑'}
# update(...)   → '보라', '흰색' 추가, '파랑'은 이미 있으므로 무시
# 결과: {'빨강', '파랑', '노랑', '보라', '흰색'}
