a = {'사과', '바나나', '딸기'}
a.add('파인애플')
a.add('바나나')
a.remove('딸기')
a.update({'망고', '사과', '키위'})
print(a)


# 정답:
# {'사과', '바나나', '파인애플', '망고', '키위'}


# 순서는 튜플이므로 무작위 출력이 됨... 순서 상관 없음
# 풀이:
# a = {'사과', '바나나', '딸기'} (Set 자료형)
# a.add('파인애플') → {'사과', '바나나', '딸기', '파인애플'}
# a.add('바나나') → 이미 존재하므로 변화 없음 (중복 불허)
# a.remove('딸기') → '딸기' 삭제 → {'사과', '바나나', '파인애플'}
# a.update({'망고', '사과', '키위'}) → 여러 요소 추가 ('사과'는 중복이라 무시됨)
# 최종 집합: {'사과', '바나나', '파인애플', '망고', '키위'}
# 집합(Set)은 순서가 없으므로 출력 순서는 다를 수 있음
